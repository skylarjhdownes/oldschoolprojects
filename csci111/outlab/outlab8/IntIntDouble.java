public class IntIntDouble
{
    int int1;
    int int2;
    double double1;
    
    public IntIntDouble(int in_int1, int in_int2, double in_double1)
    {
        int1 = in_int1;
        int2 = in_int2;
        double1 = in_double1;
    }
    public int getInt1()
    {
        return int1;
    }
    public int getInt2()
    {
        return int2;
    }
    public double getDouble1()
    {
        return double1;
    }
}

